////////////////////////////////////////////////////////////////////////////////
//
// class VoidElement
//		리스트의 원소를 나타내는 클래스
//												작성자 : asura
//												Last Update : 2000. 7. 27.
//
////////////////////////////////////////////////////////////////////////////////

class VoidElement {
	public:
		VoidElement();

		void *lpElement;					// 원소
		VoidElement *lpveNext;				// 다음 원소
};

////////////////////////////////////////////////////////////////////////////////
//
// class VoidList
//		임의의 데이터형의 원소들을 가지는 리스트
//												작성자 : asura
//												Last Update : 2000. 7. 27.
//
////////////////////////////////////////////////////////////////////////////////

class VoidList {
	public:
		VoidList();							
		~VoidList();

		void Add(void *lpNewElement);			// 새로운 원소 추가

		VoidElement *lpveHead, *lpveTail;		// 리스트의 head, tail
};

////////////////////////////////////////////////////////////////////////////////
//
// class ScopeElement
//		스코프 원소를 나타내는 클래스
//												작성자 : asura
//												Last Update : 2000. 7. 27.
//
////////////////////////////////////////////////////////////////////////////////

class ScopeElement {
	public:
		ScopeElement();
		~ScopeElement();

		char *lpszId;							// 이름(ID)
		void *lpElement;						// 객체
		ScopeElement *lpseNext;					// 다음 스코프 원소
};

////////////////////////////////////////////////////////////////////////////////
//
// class Scope
//		스코프 클래스. 스코프 원소의 리스트
//												작성자 : asura
//												Last Update : 2000. 7. 27.
//
////////////////////////////////////////////////////////////////////////////////

class Scope {
	public:  
		Scope();
		~Scope();

		void Add(ScopeElement *lpseNew);		// 새로운 스코프 원소 추가
		void *Search(char *lpszId);				// 검색
		
		ScopeElement *lpseHead, *lpseTail;		// 스코프의 시작과 끝
};

#endif // _TYPES_H_ASURA_BECD_
