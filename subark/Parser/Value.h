////////////////////////////////////////////////////////////////////////////////
//
// class Value
//		Jacqueline에서 사용되는 데이터를 내부에서 다룰 때 사용할 클래스. 내부에
//		자신의 데이터형을 나타내는 멤버 변수를 가지고 있어서 delete할 때에 자동
//		으로 데이터를 담고 있는 메모리를 자동으로 free 시켜준다.
//												작성자 : asura
//												Last Update : 2000. 7. 27.
//
////////////////////////////////////////////////////////////////////////////////

class Value {
	public:
		~Value();

		DataType *lpdt;
};

////////////////////////////////////////////////////////////////////////////////
//
// class IntValue
//		정수형 값을 나타내는 클래스
//												작성자 : asura
//												Last Update : 2000. 7. 27.
//
////////////////////////////////////////////////////////////////////////////////

class IntValue : public Value {
	public:
		IntValue();

		int n;
};

////////////////////////////////////////////////////////////////////////////////
//
// class RealValue
//		실수형 값을 나타내는 클래스
//												작성자 : asura
//												Last Update : 2000. 7. 27.
//
////////////////////////////////////////////////////////////////////////////////

class RealValue : public Value {
	public:
		RealValue();

		double r;
};

////////////////////////////////////////////////////////////////////////////////
//
// class BoolValue
//		참/거짓 값을 나타내는 클래스
//												작성자 : asura
//												Last Update : 2000. 7. 27.
//
////////////////////////////////////////////////////////////////////////////////

class BoolValue : public Value {
	public:
		BoolValue();

		bool b;
};

////////////////////////////////////////////////////////////////////////////////
//
// class ArrayValue
//		배열을 나타내는 클래스. 효율성을 위해서 데이터를 byte의 배열에 저장한다.
//		만약 Object의 배열일 경우에는 Part *의 배열로 생각한다.
//												작성자 : asura
//												Last Update : 2000. 7. 27.
//
////////////////////////////////////////////////////////////////////////////////

class ArrayValue : public Value {
	public:
		ArrayValue(ArrayType *lpdtArray);

		byte *value;
};

////////////////////////////////////////////////////////////////////////////////
//
// class IntValue
//		정수형 값을 나타내는 클래스
//												작성자 : asura
//												Last Update : 2000. 7. 27.
//
////////////////////////////////////////////////////////////////////////////////

class ObjectValue : public Value {
	public:
		ObjectValue(char *lpszPartType, Part *part);

		Part *partObject;
};

